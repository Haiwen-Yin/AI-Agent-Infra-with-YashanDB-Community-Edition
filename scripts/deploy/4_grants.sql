-- ============================================================
-- 4_grants.sql — Restricted Database User & Access Control
-- AI Agent Infra with YashanDB v3.8.0
-- ============================================================
-- 
-- This script creates a restricted database user (AGENT_API) that
-- Admin/runtime services may use instead of the schema owner (AIADMIN).
-- Business Agents MUST use independently provisioned users. This account:
--   - Can EXECUTE PL/SQL API packages (AUTHID DEFINER)
--   - Can SELECT on tables (read-only access)
--   - CANNOT INSERT/UPDATE/DELETE directly on tables
--   - CANNOT CREATE/ALTER/DROP tables, views, or procedures
--   - CANNOT perform DDL of any kind
--
-- Prerequisites:
--   - Run as SYSDBA: yasql sys/password@//host:port/service as sysdba @4_grants.sql
--   - Schema owner (AIADMIN) must already exist with all objects deployed
--
-- ============================================================

-- ============================================================
-- Schema owner configuration (adjust if not using default AIADMIN)
-- ============================================================
DEFINE SCHEMA_OWNER = 'AIADMIN'
