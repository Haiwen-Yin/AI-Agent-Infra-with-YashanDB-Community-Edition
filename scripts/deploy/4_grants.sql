PROMPT ============================================================
PROMPT Creating restricted database user: AGENT_API
PROMPT ============================================================

-- Create user using the schema owner's default tablespace (auto-detected)
DECLARE
    v_ts VARCHAR2(128);
    v_count NUMBER;
    v_password VARCHAR2(64);
BEGIN
    SELECT DEFAULT_TABLESPACE INTO v_ts
    FROM DBA_USERS WHERE USERNAME = UPPER('&&SCHEMA_OWNER');
    SELECT COUNT(*) INTO v_count FROM DBA_USERS WHERE USERNAME = UPPER('&&AGENT_USER');
    IF v_count = 0 THEN
        v_password := RAWTOHEX(SYS_GUID()) || RAWTOHEX(SYS_GUID());
        EXECUTE IMMEDIATE 'CREATE USER &&AGENT_USER IDENTIFIED BY "' || v_password || '" DEFAULT TABLESPACE ' || v_ts || ' QUOTA 0 ON ' || v_ts;
    END IF;
END;
/
GRANT CREATE SESSION TO AGENT_API;

