PROMPT ============================================================
PROMPT Creating DEEP_SEC_SESSION_ROLE for End User login
PROMPT ============================================================
-- This role carries CREATE SESSION for Deep Sec End Users.
-- End Users connect via Direct Logon with this role granted
-- through their Data Role (agent_data_role / admin_data_role / pool_agent_data_role).

DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count FROM DBA_ROLES WHERE ROLE = 'DEEP_SEC_SESSION_ROLE';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'CREATE ROLE DEEP_SEC_SESSION_ROLE';
    END IF;
END;
/
GRANT CREATE SESSION TO DEEP_SEC_SESSION_ROLE;
GRANT ALTER SESSION TO DEEP_SEC_SESSION_ROLE;
GRANT DEEP_SEC_SESSION_ROLE TO &&SCHEMA_OWNER WITH ADMIN OPTION;

PROMPT Granting least-privilege Business Agent access

DECLARE
    v_sql VARCHAR2(4000);
BEGIN
    FOR r IN (
        SELECT TABLE_NAME
        FROM DBA_TABLES
        WHERE OWNER = UPPER('&&SCHEMA_OWNER')
          AND TABLE_NAME NOT IN (
              'SYSTEM_CONFIG', 'SYSTEM_USERS', 'AGENT_CREDENTIALS',
              'AGENT_PERMISSION_LOG', 'ENTITY_ACCESS_LOG', 'CONTEXT_AUDIT_LOG',
              'CONTEXT_AUDIT_RULES', 'EXECUTION_AUDIT', 'APPROVAL_REQUESTS',
              'LDAP_CONFIG', 'LDAP_SYNC_LOG', 'SKILL_ACCESS_TOKEN'
          )
    ) LOOP
        v_sql := 'GRANT SELECT, INSERT, UPDATE, DELETE ON &&SCHEMA_OWNER..' ||
                 r.TABLE_NAME || ' TO DEEP_SEC_SESSION_ROLE';
        EXECUTE IMMEDIATE v_sql;
    END LOOP;
END;
/

