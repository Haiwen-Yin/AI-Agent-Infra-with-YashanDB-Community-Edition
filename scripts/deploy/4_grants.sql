PROMPT ============================================================
PROMPT Revoking dangerous privileges (safety net)
PROMPT ============================================================
-- AGENT_API should NEVER have these. These REVOKE statements
-- are a safety net in case they were accidentally granted.

BEGIN
    FOR r IN (
        SELECT PRIVILEGE FROM DBA_SYS_PRIVS
        WHERE GRANTEE = 'AGENT_API'
          AND PRIVILEGE IN (
              'CREATE TABLE', 'CREATE VIEW', 'CREATE PROCEDURE', 'CREATE TRIGGER',
              'CREATE SEQUENCE', 'CREATE TYPE', 'ALTER ANY TABLE', 'DROP ANY TABLE',
              'INSERT ANY TABLE', 'UPDATE ANY TABLE', 'DELETE ANY TABLE'
          )
    ) LOOP
        EXECUTE IMMEDIATE 'REVOKE ' || r.PRIVILEGE || ' FROM AGENT_API';
    END LOOP;
END;
/

