CREATE TABLE EXECUTION_JOBS (
    JOB_ID VARCHAR(64) PRIMARY KEY, JOB_TYPE VARCHAR(32) NOT NULL,
    STATUS VARCHAR(24) NOT NULL, AGENT_ID VARCHAR(64) NOT NULL,
    PAYLOAD_JSON JSON NOT NULL, RESULT_JSON JSON, ERROR_MESSAGE VARCHAR(2000),
    IDEMPOTENCY_KEY VARCHAR(128) NOT NULL UNIQUE,
    ATTEMPT_COUNT INTEGER DEFAULT 0 NOT NULL, MAX_ATTEMPTS INTEGER DEFAULT 3 NOT NULL,
    LEASE_TOKEN VARCHAR(128), LEASE_OWNER VARCHAR(128), LEASE_UNTIL TIMESTAMP,
    REQUIRES_APPROVAL CHAR(1) DEFAULT 'Y' NOT NULL, APPROVED_BY VARCHAR(64), APPROVED_AT TIMESTAMP,
    CANCEL_REQUESTED CHAR(1) DEFAULT 'N' NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL, COMPLETED_AT TIMESTAMP,
    CONSTRAINT CK_EJ_STATUS CHECK (STATUS IN ('WAITING_APPROVAL','PENDING','RUNNING','RETRY','SUCCEEDED','FAILED','CANCELLED','REJECTED'))
);
CREATE INDEX IDX_EJ_CLAIM ON EXECUTION_JOBS(STATUS, CREATED_AT);
CREATE INDEX IDX_EJ_LEASE ON EXECUTION_JOBS(STATUS, LEASE_UNTIL);

