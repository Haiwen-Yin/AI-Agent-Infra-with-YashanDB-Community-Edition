    CONSTRAINT FK_EA_JOB FOREIGN KEY (JOB_ID) REFERENCES EXECUTION_JOBS(JOB_ID) ON DELETE CASCADE,
    CONSTRAINT UK_EA_ATTEMPT UNIQUE (JOB_ID, ATTEMPT_NUMBER)
);

